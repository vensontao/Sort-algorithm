#include <stdio.h>

//==============================================================
//   快速排序算法
//==============================================================
void Quick_Sort(int a[], int low, int high)
{
    if (low < high)
    {
        int i = low;
        int j = high;
        int temp = a[i];
        while (i < j)
        {
            while(i < j && a[j] >= temp) // 从右向左找第一个小于temp的数
                j--;
            if(i < j)
                a[i++] = a[j];

            while(i < j && a[i] < temp) // 从左向右找第一个大于等于temp的数
                i++;
            if(i < j)
                a[j--] = a[i];
        }
        a[i] = temp;
        Quick_Sort(a, low, i - 1);   // 递归调用 
        Quick_Sort(a, i + 1, high);
    }
}

//===================================================================================
// 打印输出数组元素, 其中n为数组元素个数，0~n-1；
//===================================================================================
void PrintArray(int arr[], int n)
{
    int i;
    for (i = 0; i < n; i++)
    {
        printf(" %d ", arr[i]);
    }
    printf("\n");
}

